#include <stdio.h>

/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: a + b
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subs two numbers
 * @a: first number
 * @b: second number
 * Return: a - b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul two numbers
 * @a: first number
 * @b: second number
 * Return: a * b
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - adds two numbers
 * @a: first number
 * @b: second number
 * Return: a / b
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - mod two numbers
 * @a: first number
 * @b: second number
 * Return: a + b
*/
int mod(int a, int b)
{
	return (a % b);
}
